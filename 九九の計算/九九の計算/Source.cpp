#include <stdio.h>

int main(void)
{
	int a[9]{ 1,2,3,4,5,6,7,8,9 };//配列要素に１つずつ値を代入する。
	int b[9]{ 1,2,3,4,5,6,7,8,9 };//配列要素に１つずつ値を代入する。
	int i, j;

	for (i = 0, j = 0; i <= 8; i++) {//繰り返して宣言された値を出力する。
		
		
		for (j = 0; j <= 8; j++) {//繰り返して宣言された値を出力する。
			
			printf(" %d", a[i] * b[j]);//九九の計算をする。
			
			if (a[i] * b[j] <= 9) {//計算した値が９以下なら１マスあける。
				printf(" ");
			}

		}
		printf("\n");//１つの段が終了したら行を変える。
	}

	return 0;
}
